"""
Program determine sign of number by comparing with zero
"""


# Enter the variable num
num = int(input('Enter your number: '))

# Compairing number with zero and output the result
if num > 0:
    print("Your number's sign is 1")
elif num < 0:
    print("Your number's sign is -1")
else:
    print("Your number's sign is 0")



